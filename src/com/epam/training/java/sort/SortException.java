package com.epam.training.java.sort;

public class SortException extends Exception {
	private static final long serialVersionUID = 1L;

	public SortException() {
		super();
	}

	public SortException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public SortException(String arg0) {
		super(arg0);
	}

	public SortException(Throwable arg0) {
		super(arg0);
	}

}
